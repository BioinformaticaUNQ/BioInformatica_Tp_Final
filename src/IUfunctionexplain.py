## onHandlerVerSecuencia()
### crea un dialogo de mensaje, en el que muestra la secuencia ingresada.

## onHandlerVerProteina()
### crea un dialogo de mensaje, en el que muestra la proteina ingresada.

## onHandleCargar()
### Si el nombre del archivo especificado existe, analiza la secuencia fasta, en caso de ser correcta habilita los botones
#### ver proteina y ver secuencia.
### Si el nombre no existe muestra un dialogo de mensaje, con error de archivo inexistente.

